# strings
my_str = "Kevin"
print(f"String: {my_str}")

# numeric types
my_int = 7
print(f"Integer: {my_int}")

my_float = 3.498
print(f"Float: {my_float}")

my_complex = complex(4.0, 3.2)
print(f"Complex: {my_complex}")

# sequences
my_tuple = (4, 3)
print(f"Tuple: {my_tuple}")

my_range = range(1, 10)
for i in my_range:
    print(f"i: {i}")

my_list = [1, 2, 3, 4, 5]
print(f"List: {my_list}")

# map
my_dict = {"apple": 3, "banana": 2}
print(f"Dict: {my_dict}")

# set types
my_set = {"3", 5, 3.2}
print(f"Set: {my_set}")

# boolean
my_bool = False
print(f"Bool: {my_bool}")

# binary types
my_bytes = b"Hello World"
print(f"Bytes: {my_bytes}")

my_bytearray = bytearray(4)
print(f"Bytearray: {my_bytearray}")
